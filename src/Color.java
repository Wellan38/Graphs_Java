/**
 * 
 * @author alexandre
 * 
 * The color of a node defines its status in a graph search algorithm.
 * 
 * A white node has not been explored yet.
 * A grey node has been explored, but not all of its neighbors.
 * A black node has been explored, as well as all of its neighbors.
 *
 */

public enum Color
{
	White,
	Grey,
	Black;
}
